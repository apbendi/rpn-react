export let add = (left, right) => left + right;
export let subtract = (left, right) => left - right;
export let multiply = (left, right) => left * right;
export let divide = (left, right) => left / right;
export let negate = number => -1 * number;
export let squareRoot = number => Math.sqrt(number);
export let square = number => number * number;
